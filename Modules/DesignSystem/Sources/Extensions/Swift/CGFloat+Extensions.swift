import Foundation

public extension CGFloat {
    
    /// `Value = 0`
    static let zero = 0.0

    /// `Value = 4`
    static let tiny = 4.0

    /// `Value = 8`
    static let small = 8.0

    /// `Value = 12`
    static let medium = 12.0

    /// `Value = 16`
    static let standard = 16.0
    
    /// `Value = 20`
    static let mediumLarge = 20.0

    /// `Value = 24`
    static let large = 24.0

    /// `Value = 32`
    static let huge = 32.0

    /// `Value = 64`
    static let massive = 64.0

}
